package ExercicoExtraParaFixacao;

public class VariaveisInteiras {

    public static void main(String[] args) {

        int idade = 20;
        short idade1 = 21;
        byte idade2 = 24;
        long idade3 = 25;

        System.out.println("Idade da katy = " + idade + " anos");
        System.out.println("Idade da Ana = " + idade1 + " anos");
        System.out.println("Idade da Beatriz = " + idade2 + " anos");
        System.out.println("Idade da Karol = " + idade3 + " anos");
    }
}
