package ExercicoExtraParaFixacao;

public class OperadoresRacionais {

    public static void main(String[] args) {

        int valor1 = 2;
        int valor2 = 1;

        System.out.println("valor1 == valor2: " + (valor1 == valor2));
        System.out.println("valor1 != valor2: " + (valor1 != valor2));
        System.out.println("valor1 > valor2: " + (valor1 > valor2));
        System.out.println("valor1 >= valor2: " + (valor1 >= valor2));
        System.out.println("valor1 > valor2: " + (valor1 > valor2));
        System.out.println("valor1 <= valor2: " + (valor1 <= valor2));

        //estes operadores retornam um boolean

    }
}
